document.getElementById('card1').addEventListener('mouseover', () => {
    document.getElementById('card1').style.boxShadow = '0 4px 8px rgba(0, 0, 0, 0.1)';
    document.getElementById('card1').style.transform = 'translateY(-10px)';
});

document.getElementById('card1').addEventListener('mouseout', () => {
    document.getElementById('card1').style.boxShadow = 'none';
    document.getElementById('card1').style.transform = 'translateY(0)';
});

